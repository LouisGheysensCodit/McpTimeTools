namespace McpTimeServer;

/// <summary>
/// Specifies the sort order for city lists.
/// </summary>
public enum CitySortOrder
{
    /// <summary>
    /// No sorting - returns cities in insertion order.
    /// </summary>
    None,

    /// <summary>
    /// Alphabetical ascending (A-Z).
    /// </summary>
    Alphabetical,

    /// <summary>
    /// Alphabetical descending (Z-A).
    /// </summary>
    AlphabeticalDescending
}

